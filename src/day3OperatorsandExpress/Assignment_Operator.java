package day3OperatorsandExpress;

public class Assignment_Operator {

	public static void main(String[] args) {

		// Assignment Operator = += -= *= /= %=

		int a=10;
		a+=5;   // a=a+5;
		System.out.println(a);

		int b=10;
		b-=5;   // b=b-5;
		System.out.println(b);

		int c=10;
		c*=5;   // c=c*5;
		System.out.println(c);

		int d=10;
		d/=5;   // d=d/5;
		System.out.println(d);
		
		int e=10;
		e%=2;   // e=e%5;
		System.out.println(e);
		
	}

}
